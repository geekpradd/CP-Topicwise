int power(int base, int exp){
	if (exp==0)
		return 1;
	int res = power(base, exp/2);
	res = (res*res)%MOD;
	if (exp % 2)
		return (res*base)%MOD;
	return res;
}
int inverse(int n){
	return power(n, MOD-2);
}

void solve(){
	string s; cin >> s;
	int m[s.size()];
	int p[s.size()];

	for (int i=0; i<s.size(); ++i){
		if (i == 0){
			m[i] = p[i] = 0;
		}
		else {
			m[i] = m[i-1];
			p[i] = p[i-1];
		}
		if (s[i] == '-') m[i]++;
		else p[i]++; 
	}
	int res = 0;
	int best = -1;
	for (int i=0; i<s.size(); ++i){
		if (s[i] == '+') continue;
		int gotter = m[i] - p[i] - 1;

		if (gotter > best){
			res += i+1;
			best = gotter;
		}
	}
	res += s.size();

	cout << res << endl;
}	


