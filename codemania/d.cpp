int n, m;
int power(int base, int exp){
	if (exp==0)
		return 1;
	int res = power(base, exp/2);
	res = (res*res)%MOD;
	if (exp % 2)
		return (res*base)%MOD;
	return res;
}
int inverse(int n){
	return power(n, MOD-2);
}


void solve(){
	int n, x, y; cin >> n >> x >> y;
	if (n < (x+y-1) || n >  x*y){
		d1(-1); return;
	}
	vector<int> ans(n+1);
	for (int i=1; i<=y; ++i){
		ans[i] = y+1-i;
	}
	int index = y+1;
	int cur_max = y;
	int excess = n-(x+y-1);
	for (int i=2; i<=x; ++i){
		if (excess >= (y-1)){
			for (int j=y; j>=1; --j){
				ans[index] = cur_max + j;
				index++;
			}
			cur_max = cur_max + y;
			excess -= (y-1);
		}
		else {
			for (int j=excess+1; j>=1; --j){
				ans[index] = cur_max + j;
				index++;
			}
			cur_max = cur_max + excess + 1;
			excess = 0;
		}
	}
	for (int i=1; i<=n; ++i){
		d0(ans[i]);
	}
	cout << endl;
}

