int n, m;
int power(int base, int exp){
	if (exp==0)
		return 1;
	int res = power(base, exp/2);
	res = (res*res)%MOD;
	if (exp % 2)
		return (res*base)%MOD;
	return res;
}
int inverse(int n){
	return power(n, MOD-2);
}



void solve(){
	string s;
	cin >> s;
	int n = s.size();
	vector<int> count(10, 0);
	for (int i=0; i<n; ++i){
		count[s[i]-'0']++;
	}
	int ans = E13;
	for (int i=0; i<10; ++i){
		ans = min(ans, n-count[i]);
	}
	for (int i=0; i<10; ++i){
		if (count[i] == 0) continue;
		for (int j=i+1; j<10; ++j){
			if (count[j] == 0) continue;
			int res = n - count[i] - count[j];
			int slots = 0;
			int prev = -1;
			for (int k=0; k<n; ++k){
				int val = s[k] - '0';
				if (val == j || val == i){
					if (prev == -1){
						prev = val; slots++;
					}
					else {
						if (val != prev){
							slots++; prev = val;
						}
						else {
							res++;
						}
					}
				}
			}
			if (slots%2) res++;
			ans = min(ans, res);
		}
	}

	cout << ans << endl;
}

