int power(int base, int exp){
	if (exp==0)
		return 1;
	int res = power(base, exp/2);
	res = (res*res)%MOD;
	if (exp % 2)
		return (res*base)%MOD;
	return res;
}
int inverse(int n){
	return power(n, MOD-2);
}

int dfs(int cur, vector<bool> &visited, vector<int> adj[]){
	int ot = 0;
	visited[cur] = 1;
	for (int to: adj[cur]){
		if(!visited[to])
			ot += dfs(to, visited, adj);
	}

	return 1 + ot;
}

void solve(){
	int n; cin >> n;

	string a, b; cin >> a >> b;
	vector<int> adj[20];
	bool pos = 1;
	for (int i=0; i<n; ++i){
		int f = a[i] - 'a';
		int s = b[i] - 'a';
		if (f > s){
			pos = 0; break;
		}
		if (f < s){
			adj[f].pb(s);
			adj[s].pb(f);
		}
	}
	if (!pos){
		cout << -1 << endl;
		return;
	}
	int tot = 0;
	vector<bool> visited(20, false);
	for (int i=0; i<20; ++i){
		if (!visited[i] && adj[i].size()>0){
			tot += (dfs(i, visited, adj)-1);
		}
	}

	cout << tot << endl;
}

